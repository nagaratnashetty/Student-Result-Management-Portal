    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String roll=jTextField1.getText();
        String physics=jTextField2.getText();
        String maths=jTextField3.getText();
        String em=jTextField4.getText();
        String dbms=jTextField5.getText();
        String os=jTextField6.getText();
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");//create an instance with driver java data base connectivity
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp","root","Madevishetty@1234");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+roll+"'");
            if(rs.next())
            {
                st.executeUpdate("insert into resultnew(rollno,physics,maths,em,dbms,os) values('"+roll+"','"+physics+"','"+maths+"','"+em+"','"+dbms+"','"+os+"')");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"This roll number is not registered");
            }
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

